package let.youxi.entity;

public class DataPo {
	
	private Double score;//分数
	private Integer level;//游戏级别
	private Integer vLevel;//速度级别
	private Integer objNum;//方块个数
	public Double getScore() {
		return score;
	}
	public void setScore(Double score) {
		this.score = score;
	}
	public Integer getLevel() {
		return level;
	}
	public void setLevel(Integer level) {
		this.level = level;
	}
	public Integer getVLevel() {
		return vLevel;
	}
	public void setVLevel(Integer level) {
		vLevel = level;
	}
	public Integer getObjNum() {
		return objNum;
	}
	public void setObjNum(Integer objNum) {
		this.objNum = objNum;
	}
}
